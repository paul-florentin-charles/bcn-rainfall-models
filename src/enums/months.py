"""
Provides integer equivalents for months.
"""

from enum import Enum


class Month(Enum):
    """
    An Enum listing months as integers.
    """
    JANUARY: int = 1
    FEBRUARY: int = 2
    MARCH: int = 3
    APRIL: int = 4
    MAY: int = 5
    JUNE: int = 6
    JULY: int = 7
    AUGUST: int = 8
    SEPTEMBER: int = 9
    OCTOBER: int = 10
    NOVEMBER: int = 11
    DECEMBER: int = 12
